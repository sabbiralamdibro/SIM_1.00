    public ArrayList<User> userList() {

        ArrayList<User> userList = new ArrayList<>();

        try {
            Connection con = connectionDB.connection_DB();
            String query1 = "SELECT * from students ";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query1);
            User user;
            while (rs.next()) {
                user = new User(rs.getInt("st_id"), rs.getString("first_name"), rs.getString("last_name"), rs.getString("dob"), rs.getString("gender"), rs.getString("phone"), rs.getString("address"), rs.getString("email"), rs.getString("post_code"), rs.getBytes("st_image")
                , rs.getString("label"), rs.getString("batch_name"), rs.getDouble("fees"), rs.getString("schedule"), rs.getString("reg_date")
                );
                userList.add(user);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        return userList;
    }

