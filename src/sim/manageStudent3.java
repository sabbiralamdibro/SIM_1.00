    public boolean verify() {
        int atp = txtEMAIL.getText().indexOf("@");
        int dotp = txtEMAIL.getText().lastIndexOf(".");
        int len = txtEMAIL.getText().length();

        if (txtFN.getText().isEmpty() || txtLN.getText().isEmpty() || txtPHONE.getText().isEmpty() || txtADDRESS.getText().isEmpty() || txtEMAIL.getText().isEmpty() || txtPOSTCODE.getText().isEmpty()
                || lbl_img.getIcon() == null || jDateChooser1.getCalendar() == null||jDateChooser2.getCalendar() == null||COURSELABEL.getSelectedIndex()==0||BATCHNAME.getSelectedIndex()==0|| SCHEDULE.getText().isEmpty()|| FEES.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "One or more fields are empty / not selected");
            return false;
        } else if (jDateChooser1.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(rootPane, "No student from future are allow");
            return false;
        } else if (atp < 1 || dotp < atp + 2 || dotp + 2 > len) {
            JOptionPane.showMessageDialog(rootPane, "Enteralid Email address ");
            return false;
        } else {
            return true;
        }

    }


    private void cmd_save1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_save1ActionPerformed
        try {
            JFileChooser chooser = new JFileChooser();
            chooser.showOpenDialog(null);
            File f = chooser.getSelectedFile();
            filename = f.getAbsolutePath();
            ImageIcon imageIcon = new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(lbl_img.getWidth(), lbl_img.getHeight(), Image.SCALE_DEFAULT));
            lbl_img.setIcon(imageIcon);

            File image = new File(filename);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];

            for (int readNum; (readNum = fis.read(buf)) != -1;) {

                bos.write(buf, 0, readNum);
            }
            person_image = bos.toByteArray();
        } catch (Exception e) {
          e.getMessage();

        }

    }//GEN-LAST:event_cmd_save1ActionPerformed

